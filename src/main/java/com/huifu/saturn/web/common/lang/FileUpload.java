package com.huifu.saturn.web.common.lang;


/**
 * 文件上传 对象
 * 
 * @author su.zhang
 * @version $Id: FileUpload.java, v 0.1 2012-9-18 下午05:47:25 su.zhang Exp $
 */
public class FileUpload {
    private String name;
    private byte[] file;

    /**
     * @return Returns the name.
     */
    public String getName() {
        return name;
    }

    /**
     * @param name The name to set.
     */
    public void setName(String name) {
        this.name = name;
    }

    public void setFile(byte[] file) {
        this.file = file;
    }

    public byte[] getFile() {
        return file;
    }
}
